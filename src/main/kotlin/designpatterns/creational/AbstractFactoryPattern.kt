package designpatterns.creational

/*
Similar to the factory design pattern, the abstract factory design pattern is a factory of factories,
so essentially it works just like the factory pattern except it is used to wrap factories in one class.

Unlike the factory design pattern though, the abstract factory design pattern is used to wrap a family of
 families together. It was created specifically for more advanced frameworks and to help with designing
 more combust systems that might need the additional design and complexity.

 A common place to find this also is in the collections methods in Java and Kotlin.
 */